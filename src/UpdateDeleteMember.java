    //Search
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int checkid=0;
        String id = jTextField1.getText();
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where id='"+id+"'");
            while(rs.next())
            {
                checkid=1;
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField8.setText(rs.getString(5));
                jTextField8.setEditable(false);
                jTextField5.setText(rs.getString(6));
                jComboBox2.setSelectedItem(rs.getString(7));
                jComboBox1.setSelectedItem(rs.getString(8));
            }
            if(checkid==0)
            {
                JOptionPane.showMessageDialog(null, "Member ID does not Exist");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    //Update
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String Id = jTextField1.getText();
        String Name = jTextField2.getText();
        String Age = jTextField3.getText();
        String Weight = jTextField4.getText();
        String Email = jTextField5.getText();
        String GymPlan = (String)jComboBox2.getSelectedItem();
        String MembershipStatus = (String)jComboBox1.getSelectedItem();
        
        try
        {
           Connection con = ConnectionProvider.getCon();
           PreparedStatement ps = con.prepareStatement("update member set Name=?, Age=?, Weight=?, Email=?, GymPlan=?, MembershipStatus=? where Id=?");
           ps.setString(1, Name);
           ps.setString(2, Age);
           ps.setString(3, Weight);
           ps.setString(4, Email);
           ps.setString(5, GymPlan);
           ps.setString(6, MembershipStatus);
           ps.setString(7, Id);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "Successfully Updated");
           setVisible(false);
           new UpdateDeleteMember().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

